#include "libft.h"

void		ft_freetab(char ***tab)
{
	int		i;
	char	**t;

	i = 0;
	if (tab)
	{
		t = *tab;
		if (t)
		{
			while (t[i])
			{
				free(t[i]);
				t[i] = NULL;
				i++;
			}
			free(t);
			*tab = NULL;
		}
	}
}
